package com.example;

public class Calcul {

    /**
     * Adds two integers.
     * @param a first operand
     * @param b second operand
     * @return the sum of a and b
     */
    public int additionner(int a, int b) {
        return a + b;
    }

    /**
     * Multiplies two integers.
     * @param a first operand
     * @param b second operand
     * @return the product of a and b
     */
    public int multiplier(int a, int b) {
        return a * b;
    }
}
